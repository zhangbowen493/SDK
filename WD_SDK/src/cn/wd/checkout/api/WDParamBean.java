package cn.wd.checkout.api;
/**
 * H5序列化参数类
 * @author Administrator
 *
 */
public class WDParamBean {
	private String access; //支付环境
	 private String appid;	//商户id
	 private String appkey;	//商户工作秘钥
	 private String subnum;	//子商户号
	 private String amount;	//金额
	 private String subject;	
	 private String orderNo;	//订单号
	 private String channel;	//支付渠道
	 private String body;
	 private String description;
	 
	public String getAccess() {
		return access;
	}
	public void setAccess(String access) {
		this.access = access;
	}
	public String getAppid() {
		return appid;
	}
	public void setAppid(String appid) {
		this.appid = appid;
	}
	public String getAppkey() {
		return appkey;
	}
	public void setAppkey(String appkey) {
		this.appkey = appkey;
	}
	public String getSubnum() {
		return subnum;
	}
	public void setSubnum(String subnum) {
		this.subnum = subnum;
	}
	
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
	public String getOrderNo() {
		return orderNo;
	}
	public void setOrderNo(String orderNo) {
		this.orderNo = orderNo;
	}
	public String getChannel() {
		return channel;
	}
	public void setChannel(String channel) {
		this.channel = channel;
	}
	public String getBody() {
		return body;
	}
	public void setBody(String body) {
		this.body = body;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	@Override
	public String toString() {
		return "WDParamBean [access=" + access + ", appid=" + appid
				+ ", appkey=" + appkey + ", subnum=" + subnum + ", amount="
				+ amount + ", subject=" + subject + ", orderNo=" + orderNo
				+ ", channel=" + channel + ", body=" + body + ", description="
				+ description + "]";
	}
	
	 
}


